package entradaDeDados;

public class IncrementoDecremento {

	public static void main(String[] args) {

		int idade = 10;

		int novaIdade = ++idade;

		System.out.println(novaIdade);
		
		int idadeDecrease= 9;
		int novaIdadeDecrease= --idadeDecrease;
		
		
		System.out.println(novaIdadeDecrease);

	}

}
